function showSlide(index) {
  slides.forEach((s, i) => {
    s.classList.remove("active")
    if (i === index) s.classList.add("active")
  })
}

function nextSlide() {
  current = (current + 1) % slides.length
  showSlide(current)
}

// --- lifecycle
onMounted(() => {
  // Kh·ªüi t·∫°o flatpickr
  fpCheckin = flatpickr(checkinEl.value, {
