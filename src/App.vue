      query.value.checkout = dateStr
    },
  })

  // Slideshow
  slides = document.querySelectorAll(".slide")
  if (slides.length) {
    showSlide(current)
    timer = setInterval(nextSlide, 5000)
  }
})

onBeforeUnmount(() => {
  // cleanup flatpickr
  if (fpCheckin) fpCheckin.destroy()
  if (fpCheckout) fpCheckout.destroy()

  // cleanup slideshow
  if (timer) clearInterval(timer)
})





